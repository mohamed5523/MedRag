# ============================================================
# PUBLIC FUNCTION — apply_egyptian_mappings(text)
# ============================================================

# Pre-compile all patterns once at import time for performance
_COMPILED_MAPPINGS: list[list[tuple[re.Pattern, str]]] = []
for _group in ALL_MAPPINGS:
    _compiled_group = []
    for _pattern, _replacement in _group.items():
        try:
            _compiled_group.append((re.compile(_pattern, re.UNICODE), _replacement))
        except re.error:
            pass  # skip malformed patterns silently
    _COMPILED_MAPPINGS.append(_compiled_group)


def _strip_markdown(text: str) -> str:
    """Remove LLM markdown formatting that TTS should not read."""
    text = re.sub(r'\*\*([^*]+)\*\*', r'\1', text)   # **bold**
    text = re.sub(r'\*([^*]+)\*', r'\1', text)         # *italic*
    text = re.sub(r'#{1,6}\s*', '', text)               # ## headings
    text = re.sub(r'`([^`]+)`', r'\1', text)            # `code`
    text = re.sub(r'\[([^\]]+)\]\([^)]+\)', r'\1', text)  # [links](url)
    text = re.sub(r'^[-*•]\s+', '', text, flags=re.MULTILINE)  # bullet points
    return text


def apply_egyptian_mappings(text: str) -> str:
    """
    Convert MSA (Modern Standard Arabic) text to Egyptian colloquial dialect.

    Applies all mapping groups in priority order — longer/more-specific phrases
    are substituted before shorter individual words to prevent partial-match errors.

    Call this as the FIRST step in any TTS normalization pipeline.
    """
    if not text:
        return text

    # 1. Strip markdown / LLM formatting artifacts
    text = _strip_markdown(text)

    # 2. Apply each mapping group in order
    for compiled_group in _COMPILED_MAPPINGS:
        for pattern, replacement in compiled_group:
            text = pattern.sub(replacement, text)

    # 3. Collapse multiple spaces introduced by empty replacements (e.g. هل → "")
    text = re.sub(r'  +', ' ', text)
    text = text.strip()

    return text